public class IntArrayWork {
    public static void main(String[] args) {
        int[] numbers = {1,2,3,4,80,50};

      int sum = 0;

      for(int i=0; i<numbers.length; i++){
          sum = sum + numbers[i];
      }

        System.out.println("The sum is "+sum);
    }
}
