public class IfElse {
    public static void main(String[] args) {
        // Lets find great number between two number
        int n1 = 1000;
        int n2 = 200;

        if(n1>n2){
            System.out.println("Number 1 is greater with value "+n1);
        }else{
            System.out.println("Number 2 is greater with value "+n2);
        }
    }
}
