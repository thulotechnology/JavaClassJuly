public class QuRen {
    public static void main(String[] args) {
        int firstGlass = 30;
        int secondGlass = 7;

        // Swap
        int thirdGlass = firstGlass;
        firstGlass = secondGlass;
        secondGlass = thirdGlass;


        System.out.println("First Glass is "+firstGlass);
        System.out.println("Second Glass is "+secondGlass);

    }
}
